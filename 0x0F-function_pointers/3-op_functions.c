#include "3-calc.h"

/**
 * op_add - add function
 *
 * @a: 1st number
 * @b: 2nd number
 * Return: Sum (Sucess)
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 *
 * @a: 1st number
 * @b: 2nd number
 * Return: Subtract (Sucess)
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 *
 * @a: 1st number
 * @b: 2nd number
 * Return: multiply result (Sucess)
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 *
 * @a: 1st number
 * @b: 2nd number
 * Return: division result (Sucess)
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - moduls
 *
 * @a: 1st number
 * @b: 2nd number
 * Return: reminder result (Sucess)
 */

int op_mod(int a, int b)
{
	return (a % b);
}
